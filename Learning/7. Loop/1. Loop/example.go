package main

import "fmt"

func main() {

	//creating a for loop
	for i:=0 ; i < 5 ; i++ {
		fmt.Println("'i' value right now is ", i)
	}

	/*

	How this work:
	first, we declare "i"  variable as 0 ( i := 0 ),
	then, the program will check if the "i" value is lower than 5 ( i < 5 ),
	everytime "i" value is lower than 5(0,1,2,3,4), the program will run the code
	everytime the code runs, "i" value will be increased by 1 ( i++ )
	the program will stop when "i" value is 5 or bigger than 5

	 */


}
